import java.io.Serializable;


public class Aritmetica implements Serializable
{
	/**
	 * 
	 */
	private static final long serialVersionUID = -8447258468595465456L;

	public int suma()
	{
		int resultado = 4+2;
		return resultado;
	}
	
	public int resta()
	{
		int resultado = 4-2;
		return resultado;
	}
	
	public int multiplicacion()
	{
		int resultado = 4*2;
		return resultado;
	}
	
	public int divicion()
	{
		int resultado = 4/2;
		return resultado;
	}
}
